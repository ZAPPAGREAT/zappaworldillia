      document.addEventListener('DOMContentLoaded', function () {
        const form = document.querySelector('.contact-form form');
        const confirmBtn = form.querySelector('input[type="submit"]');
    
        confirmBtn.addEventListener('click', function (e) {
          e.preventDefault(); // 通常送信を止める

          // 入力値取得
          const name = form.item_name_1.value;
          const age = form.item_age.value;
          const email = form.item_mail_1.value;
          const subject = form.item_subject.value;
          const message = form.item_contents.value;

          // モーダルに値をセット
          document.getElementById('confirm-name').textContent = name;
          document.getElementById('confirm-age').textContent = age;
         document.getElementById('confirm-email').textContent = email;
          document.getElementById('confirm-subject').textContent = subject;
          document.getElementById('confirm-message').textContent = message;

          // モーダル表示
          document.getElementById('confirmModal').style.display = 'block';

         // 送信ボタン
         document.getElementById('sendBtn').onclick = function () {
          // フォームデータを取得
          const formData = new FormData(form);

          fetch('send_mail.php', {
            method: 'POST',
            body: formData
          })
          .then(response => response.json())
          .then(result => {
            if (result.status === 'success') {
              alert('送信完了しました！ありがとうございました！');
              document.getElementById('confirmModal').style.display = 'none';
              form.reset(); // フォームリセット
            } else {
              alert('送信に失敗しました：' + result.message);
            }
          })
          .catch(error => {
            alert('通信エラーが発生しました：' + error);
          });
        };


          // 戻るボタン
          document.getElementById('backBtn').onclick = function () {
            document.getElementById('confirmModal').style.display = 'none';
          };
       });
     });
    </script>

